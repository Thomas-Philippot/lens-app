import { createClient } from 'urql'
import ls from 'local-storage'

const APIURL = "https://api-mumbai.lens.dev"

export const client = createClient({
  url: APIURL,
  fetchOptions: () => {
    const token = ls.get("auth_token");
    return {
      headers: { authorization: token ? `Bearer ${token}` : '' },
    }
  },
})

